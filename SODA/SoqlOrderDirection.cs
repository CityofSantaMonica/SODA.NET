namespace SODA
{
    /// <summary>
    /// Enumeration of possible sort orders for use with a <see cref="SoqlQuery"/>.
    /// </summary>
    public enum SoqlOrderDirection
    {
        /// <summary>
        /// Value representing an ascending ordering. This is the default.
        /// </summary>
        ASC,
        /// <summary>
        /// Value representing a descending ordering.
        /// </summary>
        DESC
    }
}